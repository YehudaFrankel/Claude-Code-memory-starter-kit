#!/usr/bin/env python3
"""
bootstrap.py - Zero-setup codebase indexer for claude-memory-starter.

Scans the project on first run and generates quick_index.md —
a grouped, searchable map of every source file by type.
Gives Claude immediate codebase awareness without any manual setup.

Run: python tools/bootstrap.py
Or call from setup.py after install.
"""

import os
import json
from pathlib import Path
from datetime import datetime

SCRIPT_DIR = Path(__file__).resolve().parent
ROOT       = SCRIPT_DIR.parent

# File type groups — extensible
FILE_TYPES = {
    'Java':       ['.java'],
    'JavaScript': ['.js', '.mjs', '.cjs'],
    'TypeScript': ['.ts', '.tsx'],
    'Python':     ['.py'],
    'HTML':       ['.html', '.htm'],
    'CSS':        ['.css', '.scss', '.less'],
    'JSP':        ['.jsp', '.jspf'],
    'SQL':        ['.sql'],
    'Config':     ['.json', '.yaml', '.yml', '.xml', '.properties', '.env'],
    'Markdown':   ['.md'],
}

# Folders to skip entirely
SKIP_DIRS = {
    'node_modules', '.git', '__pycache__', '.pytest_cache',
    'dist', 'build', 'out', 'target', '.gradle', '.mvn',
    'venv', '.venv', 'env', '.env', 'coverage', '.nyc_output',
    'files', 'uploads', 'backups', '.claude', '.playwright-mcp',
}

MAX_FILES_PER_TYPE = 200  # Cap per type to avoid giant indexes


def find_memory_dir():
    for path in ROOT.rglob('MEMORY.md'):
        if '.claude' in path.parts:
            return path.parent
    return ROOT / '.claude' / 'memory'


def scan_project(root: Path) -> dict:
    """Walk the project and group files by type."""
    groups = {t: [] for t in FILE_TYPES}
    groups['Other'] = []

    ext_to_type = {}
    for type_name, exts in FILE_TYPES.items():
        for ext in exts:
            ext_to_type[ext] = type_name

    for dirpath, dirnames, filenames in os.walk(root):
        # Prune skip dirs in-place
        dirnames[:] = [d for d in dirnames if d not in SKIP_DIRS]

        for fname in filenames:
            full = Path(dirpath) / fname
            ext  = full.suffix.lower()
            rel  = full.relative_to(root).as_posix()
            t    = ext_to_type.get(ext, 'Other')

            if t == 'Other' and ext not in ('.md',):
                continue  # Skip truly unknown types

            if len(groups[t]) < MAX_FILES_PER_TYPE:
                groups[t].append(rel)

    return {k: sorted(v) for k, v in groups.items() if v}


def build_markdown(groups: dict, root: Path) -> str:
    now    = datetime.now().strftime('%Y-%m-%d %H:%M')
    total  = sum(len(v) for v in groups.values())
    counts = ', '.join(f'{len(v)} {t}' for t, v in groups.items())

    lines = [
        '# Quick Index — Codebase Map',
        f'Auto-generated: {now}',
        f'Project root: {root}',
        f'Total: {total} files ({counts})',
        '',
        'Usage: Grep(pattern=\'filename\', path=quick_index.md) to find any file.',
        '',
    ]

    for type_name, files in groups.items():
        lines.append(f'## {type_name} ({len(files)} files)')
        lines.append('')
        for f in files:
            lines.append(f'- `{f}`')
        lines.append('')

    return '\n'.join(lines)


def main():
    mem_dir    = find_memory_dir()
    index_file = mem_dir / 'quick_index.md'

    print(f'Scanning project: {ROOT}')
    groups = scan_project(ROOT)

    if not groups:
        print('No source files found.')
        return

    md = build_markdown(groups, ROOT)
    mem_dir.mkdir(parents=True, exist_ok=True)
    index_file.write_text(md, encoding='utf-8')

    total = sum(len(v) for v in groups.values())
    print(f'Quick index built: {total} files -> {index_file}')
    for t, files in groups.items():
        print(f'  {t}: {len(files)} files')

    # Register in MEMORY.md if not already there
    memory_file = mem_dir / 'MEMORY.md'
    if memory_file.exists():
        content = memory_file.read_text(encoding='utf-8')
        if 'quick_index.md' not in content:
            entry = '\n- [Quick Index](quick_index.md) — All source files grouped by type. Auto-generated by bootstrap.py. Rebuild: `python tools/bootstrap.py`'
            memory_file.write_text(content.rstrip() + entry + '\n', encoding='utf-8')
            print('Registered quick_index.md in MEMORY.md')


if __name__ == '__main__':
    main()
